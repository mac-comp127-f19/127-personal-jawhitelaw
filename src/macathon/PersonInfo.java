package macathon;

public class PersonInfo {

    private String username;
    private int age;
    private int height;
    private int weight;
    private boolean sex;

    public PersonInfo(){
    }

    public void setUsername(String s){
        username = s;
    }

    public void setAge(int a){
        age = a;
    }

    public void setHeight(int h){
        height = h;
    }

    public void setWeight(int w){
        weight = w;
    }

    public void setSex(boolean s) {
        sex = s;
    }


}
